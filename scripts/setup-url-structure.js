// Update internal links in HTML files
function updateInternalLinks() {
    const htmlFiles = [
        'index.html',
        'home.html',
        'blog.html', 
        'deals.html',
        'pages/index.html',
        'pages/blog.html',
        'pages/deals.html',
        'pages/contact.html'
    ];
    
    let totalUpdates = 0;

    htmlFiles.forEach(filePath => {
        if (!fs.existsSync(filePath)) {
            console.log(`â­ï¸  Skipping ${filePath} (not found)`);
            return;
        }

        console.log(`\nðŸ”§ Updating internal links: ${filePath}`);
        let content = fs.readFileSync(filePath, 'utf8');
        let fileUpdates = 0;

        // Update href attributes
        Object.entries(urlMappings).forEach(([oldUrl, newUrl]) => {
            const oldPattern = new RegExp(`href=["']${oldUrl.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')}["']`, 'g');
            const matches = content.match(oldPattern);
            
            if (matches) {
                content = content.replace(oldPattern, `href="${newUrl}"`);
                fileUpdates += matches.length;
                console.log(`   âœ… Updated ${matches.length} link(s): ${oldUrl} â†’ ${newUrl}`);
            }
        });

        // Update relative links to absolute clean URLs
        const relativeLinkPatterns = [
            { from: 'href="deals.html"', to: 'href="/deals/"' },
            { from: 'href="blog.html"', to: 'href="/blog/"' },
            { from: 'href="index.html"', to: 'href="/"' },
            { from: 'href="contact.html"', to: 'href="/contact/"' }
        ];

        relativeLinkPatterns.forEach(({ from, to }) => {
            const matches = content.match(new RegExp(from, 'g'));
            if (matches) {
                content = content.replace(new RegExp(from, 'g'), to);
                fileUpdates += matches.length;
                console.log(`   âœ… Cleaned ${matches.length} relative link(s): ${from} â†’ ${to}`);
            }
        });

        if (fileUpdates > 0) {
            fs.writeFileSync(filePath, content);
            console.log(`   ðŸ’¾ Saved ${filePath} with ${fileUpdates} link updates`);
            totalUpdates += fileUpdates;
        } else {
            console.log(`   â„¹ï¸  No internal links to update`);
        }
    });

    return totalUpdates;
}

